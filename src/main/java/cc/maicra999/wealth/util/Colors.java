    public static class CustomColor implements TextColor {
        private final String code;
        private final int rgb;

        public CustomColor(int r, int g, int b) {
            this.rgb = r << 16 | g << 8 | b;
            this.code = ChatColor.COLOR_CHAR + "#" + String.format("%06x", rgb);
        }

        public CustomColor(int r, int g, int b, char legacy) {
            this.rgb = r << 16 | g << 8 | b;
            this.code = String.valueOf(ChatColor.COLOR_CHAR + legacy);
        }

        @Override
        public int value() {
            return rgb;
        }

        @Override
        public String toString() {
            return code;
        }
    }
